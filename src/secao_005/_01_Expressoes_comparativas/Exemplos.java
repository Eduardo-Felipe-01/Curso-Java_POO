package secao_005._01_Expressoes_comparativas;

public class Exemplos {

    public static void main(String[] args){

        // O Valor ser fixo é intecional. É apenas para testar as expressões comparativas
        int x = 5;

        if (x > 0) {
            System.out.println("Resultado: V");
        }
        else {
            System.out.println("Resultado: F");
        }

        if (x == 3) {
            System.out.println("Resultado: V");
        }
        else {
            System.out.println("Resultado: F");
        }

        if (x <= 30) {
            System.out.println("Resultado: V");
        }
        else {
            System.out.println("Resultado: F");
        }

        if (x != 2) {
            System.out.println("Resultado: V");
        }
        else {
            System.out.println("Resultado: F");
        }
    }
}
